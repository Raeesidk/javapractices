package org.example;

public class RelationalOperators
{
    public static void main(String[] args)
    {
        int x = 786;
        int y = 986;

        System.out.println("x==y" + (x==y));
        System.out.println("x!=y" + (x!=y));
        System.out.println("x>Y"  + (x>y));
        System.out.println("x<y"  + (x<y));
        System.out.println("x>="  + (x>=y));
        System.out.println("x<=y"  + (x<=y));
    }
}
